<p>• Memory Management is the process of allocating memory space for user
programs in main memorf and managing it.</p><br>
<p>• Input/output Management is the process of controlling the operation of all
the input/output devices attached to computer.</p><br>
<p>• File Management System is the part of operating system that organizes,
stores and keeps track of computer files and folders.</p><br>
